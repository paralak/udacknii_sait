/**
 * Возвращает каноническую вакансию для исходного имени должности,
 * либо null если имя не распознано (странный/единичный случай — на ручной разбор).
 */
export function normalizePosition(rawName: string | null | undefined): string | null {
  const key = (rawName || '').trim();
  if (!key) return null;
  return POSITION_MAP[key] ?? null;
}
